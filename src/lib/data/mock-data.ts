const pad = (n: number) => n.toString().padStart(2, "0");
const dt = (offsetDays: number, hour: number, minute = 0) => {
  const d = new Date(today);
  d.setDate(d.getDate() + offsetDays);
  return `${d.getFullYear()}-${pad(d.getMonth() + 1)}-${pad(d.getDate())}T${pad(hour)}:${pad(minute)}:00Z`;
};

